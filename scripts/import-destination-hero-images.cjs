/**
 * Reads scripts/data/Destination_Hero_Images.xlsx (or path from argv) and writes
 * src/data/destinationHeroImages.generated.ts
 *
 * Merges spreadsheet over src/data/destinationHeroCurated.json: non-empty cells win;
 * empty cells fall back to curated; unknown slugs fall back to Picsum.
 */

const fs = require("fs");
const path = require("path");
const XLSX = require("xlsx");

const DEFAULT_INPUT = path.join(__dirname, "data", "Destination_Hero_Images.xlsx");
const OUT = path.join(__dirname, "..", "src", "data", "destinationHeroImages.generated.ts");
const CURATED = path.join(__dirname, "..", "src", "data", "destinationHeroCurated.json");
const DESTINATIONS_TS = path.join(__dirname, "..", "src", "data", "destinations.ts");

function picsum(slug) {
  return `https://picsum.photos/seed/${encodeURIComponent(slug)}/1600/720`;
}

function extractSlugs(ts) {
  const slugs = [];
  const re = /slug:\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(ts)) !== null) {
    slugs.push(m[1]);
  }
  const seen = new Set();
  const unique = [];
  for (const s of slugs) {
    if (!seen.has(s)) {
      seen.add(s);
      unique.push(s);
    }
  }
  return unique;
}

function normHeader(h) {
  return String(h ?? "")
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function slugColumnScore(h) {
  const n = normHeader(h);
  if (n === "slug") return 100;
  if (n.includes("slug")) return 80;
  if (n === "destination" || n === "code" || n === "key") return 60;
  if (n.includes("destination")) return 40;
  return 0;
}

function urlColumnScore(h) {
  const n = normHeader(h);
  if (n.includes("hero") && (n.includes("url") || n.includes("image") || n.includes("link"))) return 100;
  if (n === "url" || n === "link" || n === "image" || n === "hero") return 90;
  if (n.includes("image") && n.includes("url")) return 85;
  if (n.includes("hero")) return 70;
  if (n.includes("image")) return 50;
  if (n.includes("url") || n.includes("link")) return 40;
  return 0;
}

function pickColumn(headers, scorer) {
  let best = -1;
  let idx = -1;
  headers.forEach((h, i) => {
    const s = scorer(h);
    if (s > best) {
      best = s;
      idx = i;
    }
  });
  return best > 0 ? idx : -1;
}

function slugKey(raw) {
  return String(raw ?? "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}

function main() {
  const input = process.argv[2] ? path.resolve(process.argv[2]) : DEFAULT_INPUT;
  if (!fs.existsSync(input)) {
    console.error(`Missing file: ${input}`);
    console.error("Place Destination_Hero_Images.xlsx there, or pass a full path as the first argument.");
    process.exit(1);
  }

  let curated = {};
  if (fs.existsSync(CURATED)) {
    curated = JSON.parse(fs.readFileSync(CURATED, "utf8"));
  }

  const appSlugs = extractSlugs(fs.readFileSync(DESTINATIONS_TS, "utf8"));

  const wb = XLSX.readFile(input);
  const sheet = wb.Sheets[wb.SheetNames[0]];
  const rows = XLSX.utils.sheet_to_json(sheet, { header: 1, raw: false, defval: "" });
  if (!rows.length) {
    console.error("Sheet is empty.");
    process.exit(1);
  }

  const headers = rows[0].map((c) => String(c ?? "").trim());
  const iSlug = pickColumn(headers, slugColumnScore);
  const iUrl = pickColumn(headers, urlColumnScore);

  if (iSlug < 0 || iUrl < 0) {
    console.error("Could not detect slug and/or URL columns. Headers:", headers);
    process.exit(1);
  }

  /** @type {Record<string, string>} */
  const fromSheet = {};
  for (let r = 1; r < rows.length; r++) {
    const row = rows[r];
    if (!Array.isArray(row)) continue;
    const key = slugKey(row[iSlug]);
    const url = String(row[iUrl] ?? "").trim();
    if (!key) continue;
    if (url) fromSheet[key] = url;
  }

  /** @type {Record<string, string>} */
  const map = {};
  for (const slug of appSlugs) {
    const override = fromSheet[slug]?.trim();
    const base = curated[slug]?.trim();
    map[slug] = override || base || picsum(slug);
  }

  const keys = Object.keys(map).sort((a, b) => a.localeCompare(b));
  const lines = keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(map[k])},`);
  const body = [
    "/**",
    " * Resolved hero URLs (spreadsheet overrides + `destinationHeroCurated.json` fallback).",
    " * Generated by `npm run import:hero-images`. Refresh curated art: `node scripts/build-destination-hero-curated.mjs`.",
    " */",
    "export const DESTINATION_HERO_IMAGE_URLS: Record<string, string> = {",
    ...lines,
    "};",
    "",
  ].join("\n");

  fs.writeFileSync(OUT, body, "utf8");
  console.log(`Wrote ${OUT} (${keys.length} slugs) from ${input}`);
}

main();
