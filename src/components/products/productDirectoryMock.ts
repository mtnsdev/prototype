export function buildDirectoryCollectionRefs(
  ids: string[],
  all: DirectoryCollectionOption[]
): DirectoryProductCollectionRef[] {
  return ids.map((id) => {
    const c = all.find((x) => x.id === id);
    if (!c) return { id, name: id, scope: "private" };
    const scope: DirectoryProductCollectionRef["scope"] =
      c.scope === "private" ? "private" : (c.teamId ?? "team");
    return { id: c.id, name: c.name, scope };
  });
}
