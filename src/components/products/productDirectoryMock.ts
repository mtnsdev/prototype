export function getExternalSearchMeta(productId: string): DirectoryExternalSearchMeta | undefined {
  return EXTERNAL_SEARCH_META[productId];
}

/** Merges persisted chat/search saves over built-in demo metadata. */
export function resolveExternalSearchMeta(
  productId: string,
  persisted?: Record<string, DirectoryExternalSearchMeta>
): DirectoryExternalSearchMeta | undefined {
  const p = persisted?.[productId];
  if (p) return p;
  return EXTERNAL_SEARCH_META[productId];
}

function formatSavedAtForTooltip(iso: string): string {
  const d = new Date(iso);
  if (Number.isNaN(d.getTime())) return iso;
  return d.toLocaleString(undefined, {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
  });
}

export function externalSearchSavedTooltip(
  productId: string,
  persisted?: Record<string, DirectoryExternalSearchMeta>
): string | undefined {
  const m = resolveExternalSearchMeta(productId, persisted);
  if (!m) return undefined;
  const q = m.searchQuery ?? "external search";
  const who = m.savedBy?.trim() ? m.savedBy : "Advisor";
  const when = m.savedAt ? formatSavedAtForTooltip(m.savedAt) : "recently";
  let out = `Saved ${when} by ${who} — "${q}"`;
  if (m.sourceConversation != null) {
    out += ` · Chat #${m.sourceConversation}`;
  }
  return out;
}

export function cloneDirectoryProduct(p: DirectoryProduct): DirectoryProduct {
  return {
    ...p,
    imageGalleryUrls: p.imageGalleryUrls ? [...p.imageGalleryUrls] : undefined,
    collectionIds: [...p.collectionIds],
    collections: p.collections.map((c) => ({ ...c })),
    partnerPrograms: p.partnerPrograms.map((pp) => ({
      ...pp,
      activePromotions: pp.activePromotions.map((x) => ({ ...x })),
    })),
    commissionAdvisories: p.commissionAdvisories?.map((advisory) => ({ ...advisory })),
    repFirmLinks: (p.repFirmLinks ?? []).map((link) => ({ ...link })),
    agencyContacts: p.agencyContacts.map((c) => ({ ...c })),
  };
}

const promo = (
  id: string,
  effectiveRate: number,
  bookingStart: string,
  bookingEnd: string,
  travelStart: string,
  travelEnd: string
) => ({ id, effectiveRate, bookingStart, bookingEnd, travelStart, travelEnd });

function makeProgram(
  id: string,
  programId: string,
  programName: string,
  commissionRate: number | null,
  expiryDate: string | null,
  contact?: string,
  opts: Partial<
    Pick<DirectoryPartnerProgram, "amenities" | "amenityTags" | "activePromotions" | "registryStatus" | "status">
  > = {}
): DirectoryPartnerProgram {
  const program: DirectoryPartnerProgram = {
    id,
    name: programName,
    programId,
    programName,
    commissionRate,
    expiryDate,
    activePromotions: opts.activePromotions ?? [],
    amenities: opts.amenities ?? `${programName} partner rate and amenities apply.`,
    amenityTags: opts.amenityTags ?? [],
    commissionType: "percentage",
    registryStatus: opts.registryStatus,
    status: opts.status ?? "active",
  };
  if (contact != null && contact !== "") program.contact = contact;
  return program;
}

